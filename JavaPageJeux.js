 function play() {
        var audio = document.getElementById("chat");
        var mauvaiseReponse = document.getElementById("wrong");
        var userInput=document.getElementById("chatInput").value;
         if(userInput=="chat")
             {
              audio.play();
              document.getElementById("chatInput").value="";
             }
       else
           {
           mauvaiseReponse.play();
           }
       }
       
       
